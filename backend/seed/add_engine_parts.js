(async () => {
  const client = await pool.connect();
  try {
    // 1) מציאת/יצירת הפרק
    let ch = await client.query('SELECT id FROM chapters WHERE course_id=$1 AND title=$2', [COURSE, CH_TITLE]);
    let chId;
    if (ch.rows.length) {
      chId = ch.rows[0].id;
      console.log('Chapter exists, id', chId);
    } else {
      const maxO = await client.query('SELECT COALESCE(MAX(order_number),0)+1 AS n FROM chapters WHERE course_id=$1', [COURSE]);
      const ins = await client.query(
        `INSERT INTO chapters (course_id, title, description, order_number, is_active)
         VALUES ($1,$2,$3,$4,TRUE) RETURNING id`,
        [COURSE, CH_TITLE, CH_DESC, maxO.rows[0].n]);
      chId = ins.rows[0].id;
      console.log('Chapter created, id', chId, 'order', maxO.rows[0].n);
    }

    // 2) תוכן (UPSERT לפי chapter_id+title)
    let i = 1, added = 0, upd = 0;
    for (const it of ITEMS) {
      const ex = await client.query('SELECT id FROM learning_content WHERE chapter_id=$1 AND title=$2', [chId, it.title]);
      if (ex.rows.length) {
        await client.query('UPDATE learning_content SET content=$1, extra=$2, sort_order=$3 WHERE id=$4',
          [it.content.trim(), it.img, i, ex.rows[0].id]);
        upd++;
      } else {
        await client.query(
          `INSERT INTO learning_content (course_id, chapter_id, content_type, title, content, extra, sort_order)
           VALUES (NULL,$1,'RICH',$2,$3,$4,$5)`,
          [chId, it.title, it.content.trim(), it.img, i]);
        added++;
      }
      console.log('  ', it.title);
      i++;
    }
    console.log(`\nDone. chapter id=${chId}, content inserted=${added}, updated=${upd}`);
    // נשמור את ה-id לשימוש סקריפט השאלות
    require('fs').writeFileSync(require('path').join(__dirname, '.parts_chapter_id'), String(chId));
  } catch (e) { console.error('ERROR:', e.message); }
  finally { client.release(); await pool.end(); }
})();
