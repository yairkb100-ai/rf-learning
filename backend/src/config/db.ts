// ============================================================================
// חיבור למסד הנתונים (Database Connection)
// ----------------------------------------------------------------------------
// תפקיד הקובץ:
//   יוצר בריכת חיבורים (Connection Pool) יחידה למסד הנתונים PostgreSQL,
//   שכל הקוד במערכת משתמש בה כדי להריץ שאילתות.
//
// מה יש כאן:
//   • pool — אובייקט ה-Pool המיוצא. קוראים לו כ-pool.query(...) בכל הבקרים.
//
// הקשר במערכת:
//   פרטי החיבור (מארח, פורט, שם מסד, משתמש, סיסמה) נטענים ממשתני הסביבה
//   שבקובץ backend/.env באמצעות dotenv. כמעט כל בקר במערכת מייבא את pool מכאן.
// ============================================================================

import { Pool } from "pg";
import dotenv from "dotenv";

dotenv.config();

// בריכת חיבורים משותפת. מנהלת מספר חיבורים פתוחים למסד ומחזירה אותם לשימוש חוזר,
// כך שאין צורך לפתוח חיבור חדש לכל שאילתה.
export const pool = new Pool({
  host: process.env.DB_HOST,
  port: Number(process.env.DB_PORT),
  database: process.env.DB_NAME,
  user: process.env.DB_USER,
  password: process.env.DB_PASSWORD,
});
