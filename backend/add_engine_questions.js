(async () => {
  const client = await pool.connect();
  try {
    let added = 0, skipped = 0;
    for (const item of Q) {
      const courseId = COURSE_OF[item.ch];
      const exists = await client.query(
        'SELECT id FROM questions WHERE chapter_id=$1 AND question_text=$2', [item.ch, item.q]);
      if (exists.rows.length) { skipped++; continue; }
      await client.query('BEGIN');
      const qr = await client.query(
        `INSERT INTO questions (course_id, chapter_id, question_type, question_text, explanation, model_answer)
         VALUES ($1,$2,$3,$4,$5,$6) RETURNING id`,
        [courseId, item.ch, item.type, item.q, item.explanation || null, item.model || null]);
      const qid = qr.rows[0].id;
      if (item.opts) {
        for (const o of item.opts) {
          await client.query(
            'INSERT INTO question_options (question_id, answer_text, is_correct) VALUES ($1,$2,$3)',
            [qid, o.answer_text, o.is_correct]);
        }
      }
      await client.query('COMMIT');
      added++;
    }
    console.log(`Questions added=${added}, skipped(existing)=${skipped}, total in script=${Q.length}`);
    // סיכום לפי פרק
    const sum = await client.query(
      "SELECT chapter_id, question_type, count(*) FROM questions WHERE chapter_id BETWEEN 12 AND 22 GROUP BY chapter_id, question_type ORDER BY chapter_id");
    console.log('\n=== questions per chapter now ===');
    sum.rows.forEach(r => console.log('ch', r.chapter_id, r.question_type, r.count));
  } catch (e) { await client.query('ROLLBACK').catch(()=>{}); console.error('ERROR:', e.message); }
  finally { client.release(); await pool.end(); }
})();
